export function useTranslation() {
    const page = usePage();
    const locale = computed(() => page.props.locale || 'ar');

    const t = (key, params = {}) => {
        const keys = key.split('.');
        let value = translations[locale.value];
        
        if (!value) {
            return undefined;
        }
        
        for (const k of keys) {
            if (value && typeof value === 'object' && k in value) {
                value = value[k];
            } else {
                return undefined;
            }
        }

        // If translation not found or not a string, return undefined so fallback can be used
        if (!value || typeof value !== 'string') {
            return undefined;
        }

        // Replace parameters
        return value.replace(/\{(\w+)\}/g, (match, param) => params[param] || match);
    };

    const setLocale = (newLocale) => {
        if (newLocale === 'ar' || newLocale === 'en') {
            // Use router directly with the route name
            router.post('/locale', { locale: newLocale }, {
                preserveState: false,
                preserveScroll: false,
                onSuccess: () => {
                    // Force reload to apply locale changes
                    window.location.reload();
                },
            });
        }
    };

    return {
        locale,
        t,
        setLocale,
        isRTL: computed(() => locale.value === 'ar'),
    };
}

