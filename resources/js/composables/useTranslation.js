export function useTranslation() {
    const page = usePage();
    const locale = computed(() => page.props.locale || 'ar');

    const t = (key, params = {}) => {
        const keys = key.split('.');
        let value = translations[locale.value];
        
        for (const k of keys) {
            value = value?.[k];
        }

        if (!value) {
            return key;
        }

        // Replace parameters
        return typeof value === 'string' 
            ? value.replace(/\{(\w+)\}/g, (match, param) => params[param] || match)
            : value;
    };

    const setLocale = (newLocale) => {
        if (newLocale === 'ar' || newLocale === 'en') {
            // Use router directly with the route name
            router.post('/locale', { locale: newLocale }, {
                preserveState: false,
                preserveScroll: false,
                onSuccess: () => {
                    // Force reload to apply locale changes
                    window.location.reload();
                },
            });
        }
    };

    return {
        locale,
        t,
        setLocale,
        isRTL: computed(() => locale.value === 'ar'),
    };
}

